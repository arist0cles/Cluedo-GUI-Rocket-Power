package colorschemes;

import java.awt.Color;
/**
 * This abstract class represents a colorscheme the user can choose at the start of the game
 * It changes the colors of different aspects of the GUI
 * */
public abstract class ColorScheme {
	public Color DOOR;
	public Color HALLWAY;
	public Color ROOM;
	public Color WALL;
	public Color GRID;
	public Color BACKGROUND;
}
